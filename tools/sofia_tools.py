# Approval queue intake
# ---------------------------------------------------------------------------

@tool("Queue Post for Client Approval")
def sofia_queue_for_approval(
    business_key: str,
    subject: str,
    content: str,
    platforms: str,
    intent: str = "inform",
    image_url: str = "",
    video_url: str = "",
    scheduled_for: str = "",
    confidence: float = 0.85,
) -> str:
    """Queue a finished post into the approval queue for the weekly digest.

    Sofia calls this once a piece of content is fully drafted (caption +
    media URL + schedule). The artifact lands in pending_approval status,
    surfaces in the next Sunday 6 PM CT digest email to the client, and only
    publishes after the client taps Approve.

    Args:
        business_key: Tenant key (e.g. 'paper_and_purpose').
        subject: One-line headline / internal title for the digest item
            (e.g. 'IG Reel + TikTok cross-post: launch teaser').
        content: The actual post copy (caption + hashtags).
        platforms: Comma-separated channel list (e.g. 'instagram,tiktok').
        intent: One of inform / educate / nurture / close / retain / alert.
            Default 'inform'.
        image_url: Optional preview image URL — shown inline in the digest.
        video_url: Optional video URL — referenced in metadata.
        scheduled_for: Human-readable schedule (e.g. 'Tue, Sep 8 2026 — 7:08 AM CT').
        confidence: Self-assessed confidence 0–1. Default 0.85.

    Returns: JSON string with the queued artifact_id on success; error string
        on failure.
    """
    business_key = (business_key or "").strip()
    subject = (subject or "").strip()
    content = (content or "").strip()
    if not (business_key and subject and content):
        return "ERROR: business_key, subject, and content are all required."
    plats = _split_csv(platforms)
    if not plats:
        return "ERROR: at least one platform is required in platforms."

    try:
        from services.artifact import create_artifact
        from services.approval_queue import queue_artifact

        metadata: dict[str, Any] = {
            "platform": ", ".join(plats),
            "scheduled_for": scheduled_for or "",
        }
        if image_url:
            metadata["image_url"] = image_url
        if video_url:
            metadata["video_url"] = video_url

        # IMPORTANT: every client-facing social post Sofia queues must go
        # through the approval digest gate to reach Miriam. We explicitly
        # set risk_level="medium" so the artifact lands in pending_approval
        # status regardless of confidence — high-confidence content does NOT
        # bypass the client-approval gate. This is a hard rule of the
        # Calling Digital agency model: clients approve every post.
        artifact = create_artifact(
            agent_id="sofia",
            business_key=business_key,
            artifact_type="social_post",
            audience="public",
            intent=intent,
            content=content,
            subject=subject,
            channel_candidates=plats,
            confidence=float(confidence),
            risk_level="medium",
            metadata=metadata,
        )
        artifact_id = queue_artifact(artifact)
        return _truncate_json({
            "ok": True,
            "artifact_id": artifact_id,
            "business_key": business_key,
            "status": artifact.status,
            "risk_level": artifact.risk_level,
            "subject": subject,
        })
    except Exception as e:
        logger.exception("sofia_queue_for_approval failed")
        return f"ERROR: queue failed: {type(e).__name__}: {e}"


