"""tools/sofia_tools.py — CrewAI tool wrappers for Sofia's creative + queue surface.

Sofia (Head of Content & Creative at Calling Digital) needs three things at
runtime that the lower-level modules expose as plain Python functions:
  1. Image generation     (tools/image_gen.generate_image)
  2. Video generation     (tools/video_gen.generate_video)
  3. Social-post drafting (tools/zernio.create_zernio_draft)

This module wraps those functions as CrewAI @tool callables so Sofia can call
them mid-Crew. It also exposes a queue_for_approval tool so Sofia can submit
a finished post (caption + image + schedule + platform) directly into the
approval queue, where it is picked up by the weekly digest service and sent
to the client for review.

Errors return as strings (matches web_search_tool / tools/keyapi.py /
tools/shopify.py / tools/klaviyo.py pattern) — the LLM gets useful feedback
instead of a crashed Crew.
"""

from __future__ import annotations

import json
import logging
from typing import Any

from crewai.tools import tool

logger = logging.getLogger(__name__)


def _split_csv(s: str) -> list[str]:
    if not s:
        return []
    return [x.strip() for x in s.split(",") if x.strip()]


def _truncate_json(obj: Any, max_chars: int = 4000) -> str:
    out = json.dumps(obj, indent=2, default=str)
    if len(out) > max_chars:
        out = out[:max_chars] + "\n\n[...truncated for context budget...]"
    return out


