# Media generation
# ---------------------------------------------------------------------------

@tool("Generate Social Image")
def sofia_generate_image(
    prompt: str,
    business_key: str = "calling_digital",
    platform: str = "instagram",
    aspect_ratio: str = "",
) -> str:
    """Generate a social-media image via Replicate FLUX.

    Use this when drafting a static IG / Facebook post, a quote card, or a
    lifestyle product shot. The returned URL can then be attached to a
    Zernio draft or saved into an artifact's metadata.image_url field.

    Args:
        prompt: Visual description of the image (e.g., 'Hardcover spiral
            journal with watercolor butterflies on a linen tablecloth, soft
            natural light, neutral palette').
        business_key: Tenant key for any brand-specific style prefixing.
            Default 'calling_digital'.
        platform: Target platform — affects default aspect ratio. Defaults
            to 'instagram' (1:1). Use 'instagram_story' for 9:16, 'facebook'
            for 1:1, 'tiktok' for 9:16.
        aspect_ratio: Override aspect ratio (e.g. '1:1', '9:16', '4:5').
            Empty = use platform default.

    Returns: JSON string with `urls`, `model`, `aspect_ratio`, `prompt_used`
        on success; error string on failure.
    """
    prompt = (prompt or "").strip()
    if not prompt:
        return "ERROR: prompt is required."
    try:
        from tools.image_gen import generate_image
        result = generate_image(
            prompt=prompt,
            business_key=business_key or "",
            platform=platform or "default",
            aspect_ratio=aspect_ratio or "",
        )
        return _truncate_json(result)
    except Exception as e:
        logger.exception("sofia_generate_image failed")
        return f"ERROR: image generation failed: {type(e).__name__}: {e}"


@tool("Generate Social Video")
def sofia_generate_video(
    prompt: str,
    business_key: str = "calling_digital",
    platform: str = "instagram_reel",
    duration: int = 0,
    start_image_url: str = "",
) -> str:
    """Generate a short-form video clip via Replicate (Kling / MiniMax).

    Use this for IG Reels, TikTok, and short Facebook videos. Optionally
    pass start_image_url to do image-to-video (e.g., animate a journal
    cover shot generated by sofia_generate_image).

    Args:
        prompt: Description of the desired video content.
        business_key: Tenant key. Default 'calling_digital'.
        platform: 'instagram_reel', 'tiktok', or 'facebook' control duration
            + aspect ratio defaults.
        duration: Clip length in seconds. 0 = platform default.
        start_image_url: Optional first-frame image URL.

    Returns: JSON string with `url`, `model`, `aspect_ratio`, `duration`,
        `prompt_used` on success; error string on failure.
    """
    prompt = (prompt or "").strip()
    if not prompt:
        return "ERROR: prompt is required."
    try:
        from tools.video_gen import generate_video
        result = generate_video(
            prompt=prompt,
            business_key=business_key or "",
            platform=platform or "default",
            duration=int(duration) if duration else 0,
            start_image_url=start_image_url or None,
        )
        return _truncate_json(result)
    except Exception as e:
        logger.exception("sofia_generate_video failed")
        return f"ERROR: video generation failed: {type(e).__name__}: {e}"


