@tool("Create Zernio Social Draft")
def sofia_create_zernio_draft(
    content: str,
    platforms: str,
    media_urls: str = "",
) -> str:
    """Create a draft social post in Zernio. Draft only — does NOT publish.

    The draft sits in Zernio until an artifact is dispatched (which happens
    only after Miriam approves the post in the weekly digest).

    Args:
        content: Post caption / text body.
        platforms: Comma-separated platform list (e.g. 'instagram,tiktok').
            Supported: instagram, tiktok, facebook, linkedin, twitter,
            youtube, pinterest, threads, bluesky.
        media_urls: Optional comma-separated list of media URLs (image or
            video). Auto-detects type from extension.

    Returns: JSON string with the draft post object (id, status) on success;
        error string on failure.
    """
    content = (content or "").strip()
    if not content:
        return "ERROR: content is required."
    plats = _split_csv(platforms)
    if not plats:
        return "ERROR: at least one platform is required."

    try:
        from tools.zernio import create_zernio_draft
        media = _split_csv(media_urls) or None
        result = create_zernio_draft(content=content, platforms=plats, media_urls=media)
        return _truncate_json(result)
    except Exception as e:
        logger.exception("sofia_create_zernio_draft failed")
        return f"ERROR: Zernio draft failed: {type(e).__name__}: {e}"


